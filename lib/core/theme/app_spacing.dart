/// Consistent spacing values across the app
class AppSpacing {
  // Base Unit (4px)
  static const double unit = 4.0;
  
  // Common Spacing Values
  static const double xs = unit;          // 4px
  static const double sm = unit * 2;      // 8px
  static const double md = unit * 3;      // 12px
  static const double lg = unit * 4;      // 16px
  static const double xl = unit * 6;      // 24px
  static const double xxl = unit * 8;     // 32px
  static const double xxxl = unit * 12;   // 48px
  
  // Screen Padding
  static const double screenPaddingH = lg;     // 16px horizontal
  static const double screenPaddingV = lg;     // 16px vertical
  
  // Card Padding
  static const double cardPadding = lg;        // 16px
  static const double cardPaddingSmall = md;   // 12px
  
  // List Item Padding
  static const double listItemPaddingV = md;   // 12px vertical
  static const double listItemPaddingH = lg;   // 16px horizontal
  
  // Button Padding
  static const double buttonPaddingH = xl;     // 24px
  static const double buttonPaddingV = md;     // 12px
  
  // Icon Sizes
  static const double iconXS = 16.0;
  static const double iconSM = 20.0;
  static const double iconMD = 24.0;
  static const double iconLG = 32.0;
  static const double iconXL = 48.0;
  
  // Border Radius
  static const double radiusXS = 4.0;
  static const double radiusSM = 6.0;
  static const double radiusMD = 8.0;
  static const double radiusLG = 12.0;
  static const double radiusXL = 16.0;
  static const double radiusXXL = 24.0;
  static const double radiusFull = 9999.0;
  
  // Elevation (Shadow)
  static const double elevationSM = 1.0;
  static const double elevationMD = 2.0;
  static const double elevationLG = 4.0;
  static const double elevationXL = 8.0;
}
